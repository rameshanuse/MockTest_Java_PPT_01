/*
 	5. Write a Java program to declare and initialize variables of different data types 
 	(int, double, String) with appropriate identifiers.
 */
public class Question05 {
	public static void main(String[] args) {
		int intNum = 10;
		double doubleNum = 10.4;
		String name = "Sachin";
		
		System.out.println("Integer number is :: "+intNum);
		System.out.println("Double number is :: "+doubleNum);
		System.out.println("String is :: "+name);
	}
}
